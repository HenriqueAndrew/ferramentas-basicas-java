//Exemplo de itrerar por meio de um Array
package arraysTools;

public class IterarArray {

	public static void main(String[] args) {
		int[] todosDois = new int [10];//declaracao de array
		//loop para informar o numero 2 como elemento ate atingir o ultimo indice da array 
		for (int index = 0; index < todosDois.length; index++) {
			todosDois[index] = 2;
		}

	}

}
